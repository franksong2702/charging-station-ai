def get_scenario_prompt(scenario_type: str) -> str:
    """获取指定场景的 System Prompt"""
    if scenario_type not in SCENARIOS:
        raise ValueError(f"Unknown scenario type: {scenario_type}. "
                        f"Available: {list(SCENARIOS.keys())}")
    return SCENARIOS[scenario_type]["system_prompt"]

def get_scenario_info(scenario_type: str) -> dict:
    """获取场景的详细信息"""
    if scenario_type not in SCENARIOS:
        raise ValueError(f"Unknown scenario type: {scenario_type}")
    return {
        "name": SCENARIOS[scenario_type]["name"],
        "description": SCENARIOS[scenario_type]["description"],
        "end_conditions": SCENARIOS[scenario_type].get("end_conditions", [])
    }

def list_scenarios() -> list:
    """列出所有可用的场景"""
    return [
        {
            "type": scenario_type,
            "name": info["name"],
            "description": info["description"]
        }
        for scenario_type, info in SCENARIOS.items()
    ]
